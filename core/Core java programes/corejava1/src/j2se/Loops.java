package j2se;

class Loops
{
    public static void main(String args[])

     {
        int i=0;
		while(i<=5)
      {
      System.out.println(i);
i++;
      }

	for(i=0;i<=10;i++)
		 {
		      System.out.println(i);
	}

			do
      {
      System.out.println(i);
i++;
      }while(i<=20);

     }
    }